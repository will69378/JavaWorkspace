package com.kh.operator;

public class A_Arithemetic {

	// 산술 연산자에 대해서
	public void method() {
		int num1 = 10;
		int num2 = 3;

		System.out.println("num1 + num2 = " + (num1 + num2)); // 문자열 뒤에 덧셈 뺄셈은 () 씌워주기
		System.out.println("num1 - num2 = " + (num1 - num2));
		System.out.println("num1 * num2 = " + (num1 * num2));
		System.out.println("num1 / num2 = " + (num1 / num2));
		System.out.println("num1 % num2 = " + (num1 % num2));
	}

}
